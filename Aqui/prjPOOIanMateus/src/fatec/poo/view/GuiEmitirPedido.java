    private void btnConsultarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarPedidoActionPerformed
        DecimalFormat formato  = new DecimalFormat("#,##0.00");
        
        if (!txtNumPedido.getText().matches("[0-9]+")) {
            JOptionPane.showMessageDialog(null, "NÃºmero de Pedido invÃ¡lido!", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int i;
        for (i=0; i<cadPedido.size(); i++) {
            if (cadPedido.get(i).getNumero().equals(txtNumPedido.getText())) {
                break;
            }
        }
        
        if (i < cadPedido.size()) {
            posPed = i;
        }
        else {
            posPed = -1;
        }
        
        if (posPed >= 0) {
            pedido = (Pedido)cadPedido.get(posPed);
            
            txtCPFCliente.setText(pedido.getCliente().getCpf());
            lblNomeCliente.setText(pedido.getCliente().getNome());
            txtCPFVendedor.setText(pedido.getVendedor().getCpf());
            lblNomeVendedor.setText(pedido.getVendedor().getNome());
            txtDataPedido.setText(pedido.getDataEmissao());
            if(pedido.isFormaPagto() == true){
                cbxFormaPagamento.getItemAt(0);
            }
            else{
                cbxFormaPagamento.getItemAt(1);
            }
                
            lblValorTotalPed.setText(String.valueOf(pedido.calcValorTotal()));
            lblQtdeItensPed.setText(String.valueOf(pedido.getItensPedido().size()));
            
            txtCodProduto.setEnabled(true);
            btnConsultarProduto.setEnabled(true);
            btnRemoverItem.setEnabled(true);
            btnIncluir.setEnabled(false);
            btnAlterar.setEnabled(true);
            btnExcluir.setEnabled(true);
            
            //verificar data - inicio
            try{
                Date data = null;
                String dataTexto = txtDataPedido.getText();
                SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
                
                format.setLenient(false);
                data = format.parse(dataTexto);
                pedido = new Pedido(txtNumPedido.getText(),txtDataPedido.getText());
                
                if( cbxFormaPagamento.getSelectedIndex() == 0){
                    pedido.setFormaPagto(true);
                }
                else{
                    pedido.setFormaPagto(false);
                }
            
                txtDataPedido.setEnabled(false);
                btnConsultarCliente.setEnabled(true);
                txtCPFCliente.setEnabled(true);   
                txtNumPedido.setEnabled(false);
                cbxFormaPagamento.setEnabled(false);
            } 
            catch (ParseException e) {
                JOptionPane.showMessageDialog(null, "Data InvÃ¡lida", "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }
            //verificar data - Fim

            //exibir na tabela - inicio
            seqItemPed = pedido.getItensPedido().get(pedido.getItensPedido().size() - 1).getSequencia() + 1;
            itensPedidoAntesAlterar = new ArrayList<ItemPedido>();
            
            for(i = 0; i < pedido.getItensPedido().size(); i++){
                
                String linha[] = {
                    pedido.getItensPedido().get(i).getProduto().getCodigo(),
                    pedido.getItensPedido().get(i).getProduto().getDescricao(),
                    formato.format(pedido.getItensPedido().get(i).getProduto().getPreco()),
                    formato.format(pedido.getItensPedido().get(i).getQtdeVendida()),
                    formato.format(pedido.getItensPedido().get(i).calcValor())
                };
                
                modTblProd.addRow(linha);
                
                itensPedidoAntesAlterar.add(new ItemPedido(pedido.getItensPedido().get(i)));
            }
            //exibir na tabela - fim
        }
        else {
            seqItemPed = 0;
            
            txtDataPedido.setEnabled(true);
            cbxFormaPagamento.setEnabled(true);
            txtCPFCliente.setEnabled(true);
            btnConsultarCliente.setEnabled(true);
        }
            
        btnConsultarPedido.setEnabled(false);

    }//GEN-LAST:event_btnConsultarPedidoActionPerformed

